#!/usr/bin/env node
// Regenerate openspec/specs/README.md — an always-fresh inventory of
// domain/capability specs, sorted alphabetically by slug.
//
// For each folder matching `openspec/specs/<slug>/spec.md` the script
// records:
//   - the slug (folder name)
//   - the spec title (first `# <Title>` heading)
//   - a one-line purpose extracted from the first prose line under the
//     `## Purpose` heading.
//
// Idempotent — running twice produces a byte-identical file.

import {
  existsSync,
  readdirSync,
  readFileSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, "..");
const SPECS_DIR = join(REPO_ROOT, "openspec", "specs");
const README = join(SPECS_DIR, "README.md");

function truncate(text, max = 160) {
  const trimmed = text.trim();
  if (trimmed.length <= max) return trimmed;
  return trimmed.slice(0, max - 1) + "…";
}

function escapeTableCell(text) {
  return text.replace(/\\/g, "\\\\").replace(/\|/g, "\\|");
}

function firstHeading(src) {
  const match = /^# (.+)$/m.exec(src);
  return match ? match[1].trim() : "";
}

function purposeLine(src) {
  const lines = src.split("\n");
  const start = lines.findIndex((l) => /^## Purpose\s*$/.test(l.trim()));
  if (start === -1) return "";
  for (const raw of lines.slice(start + 1)) {
    const line = raw.trim();
    if (line.length === 0) continue;
    if (/^#{1,6} /.test(line)) break;
    return truncate(line.replace(/\*\*/g, ""));
  }
  return "";
}

export function buildInventory() {
  if (!existsSync(SPECS_DIR)) return "";

  const rows = [];
  for (const entry of readdirSync(SPECS_DIR)) {
    const full = join(SPECS_DIR, entry);
    if (!statSync(full).isDirectory()) continue;
    const specPath = join(full, "spec.md");
    if (!existsSync(specPath)) continue;

    const src = readFileSync(specPath, "utf8");
    rows.push({
      slug: entry,
      title: firstHeading(src),
      purpose: purposeLine(src),
    });
  }

  rows.sort((a, b) => a.slug.localeCompare(b.slug));

  const header = [
    "# Spec Inventory",
    "",
    "Auto-generated by `scripts/generate-specs-inventory.mjs`. Do NOT hand-edit.",
    "Run `pnpm specs:inventory` after adding, renaming, or repurposing a spec.",
    "CI verifies freshness via `pnpm lint:specs-inventory`.",
    "",
    `${rows.length} specs.`,
    "",
    "| Spec | Title | Purpose |",
    "| ---- | ----- | ------- |",
  ];
  const lines = rows.map(
    ({ slug, title, purpose }) =>
      `| [\`${slug}\`](./${slug}/spec.md) | ${escapeTableCell(title)} | ${escapeTableCell(purpose)} |`
  );

  return [...header, ...lines, ""].join("\n");
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  const content = buildInventory();
  if (!content) {
    console.error(`[generate-specs-inventory] ${SPECS_DIR} missing`);
    process.exit(1);
  }
  writeFileSync(README, content);
  console.log(`wrote ${README}`);
}
