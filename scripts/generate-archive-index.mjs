#!/usr/bin/env node
// Regenerate openspec/changes/archive/README.md — an always-fresh index
// of archived OpenSpec changes, sorted reverse-chronologically.
//
// For each folder matching `YYYY-MM-DD-<slug>/` the script records:
//   - the date (from the folder prefix, which is the archive-commit date)
//   - the slug (humanised for display)
//   - a one-line summary extracted from the proposal's first paragraph
//     below the `# <Title>` heading, or the heading itself as a fallback.
//
// Idempotent — running twice produces a byte-identical file.

import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, "..");
const ARCHIVE_DIR = join(REPO_ROOT, "openspec", "changes", "archive");
const README = join(ARCHIVE_DIR, "README.md");
const folderDateRe = /^(\d{4}-\d{2}-\d{2})-([a-z0-9][a-z0-9-]*)$/;

function truncate(text, max = 160) {
  const trimmed = text.trim();
  if (trimmed.length <= max) return trimmed;
  return trimmed.slice(0, max - 1) + "…";
}

function firstProse(src) {
  // Walk lines, skip metadata (> Completed:, > Synced:), find the first
  // non-heading, non-blank prose line. Strip inline markdown bold.
  for (const raw of src.split("\n")) {
    const line = raw.trim();
    if (line.length === 0) continue;
    if (/^>/.test(line)) continue;
    if (/^#{1,6} /.test(line)) continue;
    if (/^[-*] /.test(line)) continue;
    return truncate(line.replace(/\*\*/g, "").replace(/`/g, ""));
  }
  return "";
}

function firstHeading(src) {
  const match = /^# (.+)$/m.exec(src);
  return match ? match[1].replace(/^Proposal:\s*/i, "") : "";
}

export function buildIndex() {
  if (!existsSync(ARCHIVE_DIR)) return "";

  const rows = [];
  for (const entry of readdirSync(ARCHIVE_DIR)) {
    const match = folderDateRe.exec(entry);
    if (!match) continue;
    const full = join(ARCHIVE_DIR, entry);
    if (!statSync(full).isDirectory()) continue;

    const [, date, slug] = match;
    const proposalPath = join(full, "proposal.md");
    let summary = "";
    if (existsSync(proposalPath)) {
      const src = readFileSync(proposalPath, "utf8");
      summary = firstHeading(src) || firstProse(src);
    }
    rows.push({ date, slug, entry, summary });
  }

  rows.sort((a, b) => (a.date > b.date ? -1 : a.date < b.date ? 1 : a.slug.localeCompare(b.slug)));

  const header = [
    "# Archived Changes",
    "",
    "Auto-generated by `scripts/generate-archive-index.mjs`. Do NOT hand-edit.",
    "This file is regenerated by the `/opsx-archive` workflow; run",
    "`pnpm archive:index` manually if you modify archive entries out of band.",
    "CI verifies freshness via `pnpm lint:archive-index`.",
    "",
    "| Date | Change | Summary |",
    "| ---- | ------ | ------- |",
  ];
  const lines = rows.map(
    ({ date, slug, entry, summary }) =>
      `| ${date} | [\`${slug}\`](./${entry}/) | ${summary.replace(/\|/g, "\\|")} |`,
  );

  return [...header, ...lines, ""].join("\n");
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  const content = buildIndex();
  if (!content) {
    console.error(`[generate-archive-index] ${ARCHIVE_DIR} missing`);
    process.exit(1);
  }
  writeFileSync(README, content);
  console.log(`wrote ${README}`);
}
