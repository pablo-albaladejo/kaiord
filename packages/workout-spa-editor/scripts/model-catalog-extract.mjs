/**
 * Extracts chat/text model ids from the installed `@ai-sdk/*` type unions and
 * renders the runtime model catalog consumed by the SPA. Sourcing from the
 * pinned SDK means model lists track `@ai-sdk/*` bumps instead of a
 * hand-maintained enum. A free-text field in the UI covers ids newer than the
 * pin.
 */
import { readFileSync } from "node:fs";
import { createRequire } from "node:module";
import { dirname, join } from "node:path";

const requireFrom = createRequire(import.meta.url);

/** Per-provider: which union to read and which non-text ids to drop. */
export const SOURCES = {
  anthropic: { pkg: "@ai-sdk/anthropic", union: "AnthropicMessagesModelId" },
  openai: {
    pkg: "@ai-sdk/openai",
    union: "OpenAIChatModelId",
    exclude: /(audio|search-preview|transcribe|tts|image|embedding|moderation|realtime)/,
  },
  google: {
    pkg: "@ai-sdk/google",
    union: "GoogleGenerativeAIModelId",
    exclude:
      /(image|tts|audio|computer-use|robotics|embedding|imagen|veo|lyria|nano-banana|deep-research)/,
  },
};

/** Extract single-quoted literals from `type <unionName> = '...' | ...;`. */
export const parseModelIds = (dtsText, unionName) => {
  const match = new RegExp(`type ${unionName}\\s*=([^;]*);`).exec(dtsText);
  if (!match) throw new Error(`union ${unionName} not found`);
  return [...match[1].matchAll(/'([^']+)'/g)].map((m) => m[1]);
};

/** Parsed, exclusion-filtered chat ids for one provider type. */
export const chatModelIds = (type, dtsText) => {
  const { union, exclude } = SOURCES[type];
  return parseModelIds(dtsText, union).filter(
    (id) => !(exclude && exclude.test(id))
  );
};

const readSdkDts = (pkg) =>
  readFileSync(
    join(dirname(requireFrom.resolve(`${pkg}/package.json`)), "dist", "index.d.ts"),
    "utf8"
  );

/** Build the full catalog by reading each installed SDK package. */
export const extractCatalog = () => {
  const catalog = {};
  for (const type of Object.keys(SOURCES)) {
    catalog[type] = chatModelIds(type, readSdkDts(SOURCES[type].pkg)).map(
      (id) => ({ id, label: id })
    );
  }
  return catalog;
};

const renderEntries = (options) =>
  options
    .map(
      (o) =>
        `    { id: ${JSON.stringify(o.id)}, label: ${JSON.stringify(o.label)} },`
    )
    .join("\n");

/** Render the generated TS module text (must match committed bytes). */
export const renderCatalogModule = (catalog) =>
  `// AUTO-GENERATED by scripts/generate-model-catalog.mjs — DO NOT EDIT.
// Sourced from the installed @ai-sdk/* model-id type unions, filtered to
// chat/text models. Regenerate with \`pnpm generate:model-catalog\` after
// bumping @ai-sdk/*. A free-text field in the UI covers ids newer than the pin.
import type { LlmProviderType } from "../../store/ai-store-types";

export type ModelOption = { id: string; label: string };

export const MODEL_CATALOG: Record<LlmProviderType, ModelOption[]> = {
  anthropic: [
${renderEntries(catalog.anthropic)}
  ],
  openai: [
${renderEntries(catalog.openai)}
  ],
  google: [
${renderEntries(catalog.google)}
  ],
};
`;
